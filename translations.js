// Language switching functionality
class LanguageSwitcher {
  constructor() {
    this.currentLang = localStorage.getItem('language') || 'bos';
    this.init();
  }
  
  init() {
    this.loadLanguage(this.currentLang);
    this.bindEvents();
    this.updateLanguageButtons();
  }
  
  bindEvents() {
    const langButtons = document.querySelectorAll('.lang-link');
    langButtons.forEach(button => {
      button.addEventListener('click', (e) => {
        e.preventDefault();
        const lang = e.target.getAttribute('data-lang');
        this.switchLanguage(lang);
      });
    });
  }
  
  switchLanguage(lang) {
    this.currentLang = lang;
    localStorage.setItem('language', lang);
    this.loadLanguage(lang);
    this.updateLanguageButtons();
  }
  
  updateLanguageButtons() {
    const langButtons = document.querySelectorAll('.lang-link');
    langButtons.forEach(button => {
      const buttonLang = button.getAttribute('data-lang');
      button.classList.toggle('active', buttonLang === this.currentLang);
    });
  }
  
  loadLanguage(lang) {
    const texts = translations[lang];
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
      const key = element.getAttribute('data-translate');
      if (texts[key]) {
        if (element.tagName === 'INPUT' && element.type === 'submit') {
          element.value = texts[key];
        } else if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
          element.placeholder = texts[key];
        } else {
          element.textContent = texts[key];
        }
      }
    });
    
    // Update document language attribute
    document.documentElement.lang = lang === 'bos' ? 'bs' : 'en';
  }
}

// Initialize language switcher when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
  new LanguageSwitcher();
});
